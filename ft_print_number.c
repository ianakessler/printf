#include "ft_printf.h"


int	ft_print_number(int n)
{
	int i;

	i = 0;
	if (n < -2147483647)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	else if (n < 0)
	{
		n = -n;
		write(1, "-", 1);
		i++;
	}
	if (n > 9)
	{
		i += ft_print_number(n / 10);
	}
	write(1, &"0123456789"[n % 10], 1);
	i++;
	return (i);
}

